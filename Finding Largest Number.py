# This is a sample program to find the largest number between the two numbers.
# The program can also be able to compare three numbers as well, all it requires is providing and declaring three numbers in the input.
a=int(input("Enter the first number"))

b=int(input("Enter the second number"))

if a>b:
  print("The largest number is :",a)
else:
  print("The largest number is :",b)
